public class Calculator {



    public int operAdunare(int a, int b ){
        return a + b;
    }
    public int operScadere(int a, int b){
        return a - b;
    }
    public int operInmultire(int a, int b){
        return a * b;
    }
    public float operImpartire(float a, float b){
        return a / b;
    }
}
